"""Правовая оговорка и происхождение данных (единый источник истины).

Используется и при сборке перечня (main.py), и в отчётах скринера (report.py),
чтобы результаты не воспринимались как юридическое заключение.
"""
from __future__ import annotations

from datetime import datetime
from pathlib import Path
from typing import List

# Происхождение данных: на каких ОФИЦИАЛЬНЫХ источниках основан перечень.
SOURCES_PROVENANCE: List[tuple[str, str]] = [
    ("Минюст России — перечень экстремистских организаций",
     "https://minjust.gov.ru/ru/documents/7822/"),
    ("ФСБ России — единый список террористических организаций",
     "http://www.fsb.ru/fsb/npd/terror.htm"),
    ("Росфинмониторинг — перечень террористов и экстремистов",
     "https://www.fedsfm.ru/documents/terrorists-catalog-portal-act"),
    ("Минюст России — реестр иностранных агентов",
     "https://minjust.gov.ru/ru/pages/reestr-inostryannykh-agentov/"),
]

DISCLAIMER = (
    "ПРАВОВАЯ ОГОВОРКА\n"
    "1. Данные получены автоматизированно из публичных официальных реестров "
    "(см. «Источники» ниже) и приведены «как есть». Возможны расхождения с "
    "первоисточником из-за изменений на сайтах и особенностей разбора.\n"
    "2. Категории и правовые статусы («экстремистская», «иностранный агент», "
    "«запрещена в РФ» и т.п.) ВОСПРОИЗВОДЯТСЯ из источников и НЕ являются "
    "самостоятельной правовой квалификацией. Перед использованием их должны "
    "проверить и заверить юридическая/комплаенс-служба заказчика по "
    "первоисточнику.\n"
    "3. Совпадения по ФИО — это КАНДИДАТЫ для ручной проверки, а не "
    "установление личности: однофамильцы дают ложные срабатывания. Точная "
    "идентификация физлиц требует сверки доп. реквизитов (дата рождения и пр.).\n"
    "4. Инструмент предназначен для поддержки принятия решений, а не для "
    "автоматических выводов или санкций.\n"
)


def provenance_text() -> str:
    """Текстовый блок «Источники» с URL."""
    lines = ["ИСТОЧНИКИ (официальные реестры):"]
    lines += [f"  - {name}: {url}" for name, url in SOURCES_PROVENANCE]
    return "\n".join(lines)


def full_notice() -> str:
    """Полный текст: метка времени + оговорка + источники."""
    stamp = datetime.now().strftime("%Y-%m-%d %H:%M")
    return (
        f"Сформировано: {stamp}\n\n{DISCLAIMER}\n{provenance_text()}\n"
    )


def write_notice(directory: str, filename: str = "DISCLAIMER.txt") -> Path:
    """Записать оговорку рядом с результатами."""
    out = Path(directory)
    out.mkdir(parents=True, exist_ok=True)
    path = out / filename
    path.write_text(full_notice(), encoding="utf-8")
    return path
